        public static IReadOnlyList<GridCellData> DecodeGridCells()
        {
            var cells = new List<GridCellData>(PackedCellTriples.Length / 3);

            for (int i = 0; i < PackedCellTriples.Length; i += 3)
            {
                int packedXY = PackedCellTriples[i];
                int z = PackedCellTriples[i + 1];
                int packedTileAndRotation = PackedCellTriples[i + 2];

                int x = (short)(packedXY & 0xFFFF);
                int y = (short)((packedXY >> 16) & 0xFFFF);
                int tileId = packedTileAndRotation & 0xFFFF;
                int orientation = packedTileAndRotation >> 16;

                cells.Add(new GridCellData(new Vector3Int(x, y, z), tileId, orientation));
            }

            return cells;
        }
